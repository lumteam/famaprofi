<h1 style="text-align: justify;">Покраска кухонных столешниц </h1>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Для кухонных столешниц мы предлагаем <a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/mebel_i_stoleshnitsy/2052_maslo_dlya_rabochikh_poverkhnostey/"><span style="color: #f7941d;">2052</span></a>&nbsp;в бесцветном варианте.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Дело в том, что поверхность столешниц подвержена серьезным механическим нагрузкам и требует довольно частого обновления. Проще всего вернуть поверхности первоначальное состояние можно именно при использовании бесцветного масла.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Поверхность предварительно шлифуется абразивом с зернистостью Р-180. Затем наносится два слоя масла.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Первый слой наносим кисточкой/ валиком/ тканью ровным слоем с небольшим излишком . Ждем 30-40 мин и полируем обычной бытовой шлифовальной&nbsp; маш
</p>
<p style="text-align: justify;">
	 иной&nbsp;орбитального&nbsp;типа с использованием белого зеленого пада(для сосны подойдет белый пад).&nbsp; Работать машинкой следует до тех пор, пока на поверхности не перестанут оставаться следы от пада.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Второй слой наносим кисточкой через сутки.&nbsp; Ждем 20-30&nbsp; мин и практически в ноль растираем тканью(вафельным полотенцем), на сосне второй слой можно располировать белым падом
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 ВАЖНО:&nbsp;Второй слой должен быть очень тонким, иначе поверхность может легко царапаться и долго не сохнуть(может вообще не высохнуть).Если такое произошло- т.е. поверхность уже целый месяц «липнет», нужно ручным шлифовальщиком Р-240(или зеленым падом) убрать излишки масла (до сухой поверхности), нанести тонкий слой <a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/mebel_i_stoleshnitsy/2052_maslo_dlya_rabochikh_poverkhnostey/"><span style="color: #f7941d;">2052</span></a>, выждать 15 мин и насухо растереть полотенцем.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Восстановить поврежденную поверхность очень легко. Зачищаем поверхность наждачной бумагой Р 180. Наносим масло на поврежденный участок, делаем паузу в 20 мин , которая нужна для того чтобы масло впиталось в поверхность дерева. Полируем белым падом, а излишки удаляем тканью.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Р.S. Вариант покрытия столешницы &nbsp;первым слоем цветного <a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/mebel_i_stoleshnitsy/2052_maslo_dlya_rabochikh_poverkhnostey/"><span style="color: #f7941d;">2052</span></a> и вторым бесцветным 2052 не проходит, потому что второй слой получается очень тонкий, и быстро изнашивается. Поэтому, эксплуатационным нагрузкам будет подвержен &nbsp;цветной слой, который восстановить&nbsp; сложнее, а в результате частичной реставрации&nbsp;&nbsp;будет нарушена целостность оптики всей поверхности.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Но если очень сильно нужно получить поверхность в цвете, то мы рекомендуем следующий вариант окраски обеденного стола и рабочей поверхности на кухне
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Тон задается колерованным маслом для интерьера <a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/pol_parket_lestnitsy/8500_biofa_tsvetnoe_maslo_dlya_interera/"><span style="color: #f7941d;">8500</span></a>, а сверху наносится два прозрачных слоя 5045 Твердое масло на водной основе.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 С поверхности, окрашенной продуктом <a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/steny_i_potolki/5045_tverdoe_maslo_na_vodnoy_osnove_shelkovisto_matovoe/"><span style="color: #f7941d;">5045</span></a>/<a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/steny_i_potolki/5245_tverdoe_maslo_na_vodnoy_osnove_matovoe/"><span style="color: #f7941d;">5245</span></a> бесследно удаляются практически все загрязнения от пищевых жидкостей. Мы проводили эксперимент со следующими продуктами: чай, кофе, сок из пакета, пиво, вино и даже, свежевыжатый апельсиновый сок и кетчуп для мариновки мяса для шашлыка. Причем образовавшиеся на окрашенной поверхности пятна мы удалили через сутки!
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Важно! Масло серии <a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/pol_parket_lestnitsy/8500_biofa_tsvetnoe_maslo_dlya_interera/"><span style="color: #f7941d;">8500</span></a> нужно располировать через 30 минут после нанесения. Через сутки, убедившись, что поверхность сухая, сделать межслойное шлифование абразивом Р -500, удалить пыль и нанести <span style="color: #f7941d;"><a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/steny_i_potolki/5045_tverdoe_maslo_na_vodnoy_osnove_shelkovisto_matovoe/"><span style="color: #f7941d;">5045</span></a></span> кистью или валиком из СИНТЕТИЧЕСКОГО ворса высотой 4 мм. Хорошо подходит валик из микрофибры. Через 6-8 часов можно нанести второй слой масла арт.5045.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Масло <a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/steny_i_potolki/5045_tverdoe_maslo_na_vodnoy_osnove_shelkovisto_matovoe/"><span style="color: #f7941d;">5045</span></a> «схватывается» за 5 -10 минут и работать с ним нужно быстро, следят за тем, чтобы в процессе окраски не оставалось полос от малярного инструмента.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Окончательная прочность покрытия происходит через 7-10 дней после окраски. Только после этого срока, поверхность можно эксплуатировать в штатном режиме.
